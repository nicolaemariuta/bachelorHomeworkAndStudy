package kruskal;

public class Muchie 
{
	int v1;

	int v2;
	int c;
	
	Muchie(int v1, int v2, int c)
	{
		this.v1 = v1;
		this.v2 = v2;
		this.c = c;
	}
	
	public int getV1() {
		return v1;
	}

	public void setV1(int v1) {
		this.v1 = v1;
	}

	public int getV2() {
		return v2;
	}

	public void setV2(int v2) {
		this.v2 = v2;
	}

	public int getC() {
		return c;
	}

	public void setC(int c) {
		this.c = c;
	}


}
